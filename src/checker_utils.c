#include "push_swap.h"

void	b_ss(t_list *a, t_list *b)
{
	swap(a);
	swap(b);
}

int	ft_strcmp(char *s1, char *s2)
{
	while ((*s1 == *s2) && (*s1 && *s2))
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}